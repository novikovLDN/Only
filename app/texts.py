RTL_MARKER = "\u200F"  # Right-to-Left Embedding


def _normalize_lang(lang: str | None) -> str:
    """Ensure lang is 'ru', 'en' or 'ar'. Default: 'ru'."""
    if not lang:
        return "ru"
    code = (lang or "")[:2].lower()
    if code == "ar":
        return "ar"
    return "en" if code == "en" else "ru"


def t(lang: str, key: str, **kwargs) -> str:
    """Get translated text. Use **kwargs for .format(). RTL prefix for Arabic."""
    lang = _normalize_lang(lang)
    texts = TEXTS.get(lang, TEXTS["ru"])
    text = texts.get(key, TEXTS["ru"].get(key, key))
    if kwargs:
        text = text.format(**kwargs)
    return RTL_MARKER + text if lang == "ar" else text
